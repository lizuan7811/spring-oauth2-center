IF EXISTS (SELECT 1 
           FROM INFORMATION_SCHEMA.TABLES 
           WHERE TABLE_TYPE='BASE TABLE' 
           AND TABLE_NAME='STOCK_HIST_DATA') 
		   
BEGIN
DROP TABLE STOCK_HIST_DATA
END

CREATE TABLE STOCK_HIST_DATA (
STOCKCODE varchar(6) NOT NULL ,
DATET varchar(20) NOT NULL,
TRANSACTVOLUME varchar(20) ,
TOTALPRICE varchar(20),
STARTPRICE varchar(20) ,
HIGHESTPRICE varchar(20) ,
LOWESTPRICE varchar(20) ,
ENDPRICE varchar(20) ,
UPANDDOWN varchar(20) ,
SHARESTRADENUM varchar(20)
primary key (STOCKCODE, DATET)
)
